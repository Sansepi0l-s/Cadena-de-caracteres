ORG 100H
.MODEL SMALL
.STACK 100H
.DATA
TEXTO DB "Introduce una frase de un maximo de 15 caracteres. por favor:$"
MAXIMOMAS1 DB 16
CARACTERESLEIDOS DB 0
CADENA DB 16 DUP (0)
.CODE
INICIO:               
MOV AX, @DATA
MOV DS, AX
XOR CX, CX

MOV AH, 9
LEA DX, TEXTO
INT 21H

MOV AH, 2
MOV DL, 10
INT 21H

MOV DL, 13
INT 21H

MOV AH, 0AH
LEA DX, MAXIMOMAS1
INT 21H

MOV AH, 2
MOV DL, 10
INT 21H

MOV DL, 13
INT 21H

MOV CL, CARACTERESLEIDOS
XOR SI, SI 
MOV SI, CX
SUB SI, 1
MOV AH, 2
BUCLE:       
    MOV DL, CADENA[SI]
    INT 21H
    DEC SI
LOOP BUCLE
MOV AH, 4CH
INT 21H
END INICIO
